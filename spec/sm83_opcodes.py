def _normalize_cycles(cycles: list[int]) -> tuple[int, ...]:
    if len(cycles) == 2:
        taken, not_taken = cycles
        return (not_taken, taken)
    return tuple(cycles)


def _infer_addressing_form(operand: Mapping[str, object]) -> AddressingForm:
    name = str(operand['name'])
    immediate = bool(operand.get('immediate', True))
    increment = bool(operand.get('increment', False))
    decrement = bool(operand.get('decrement', False))

    if name in _CONDITION_CODES:
        return AddressingForm.CONDITION_CODE
    if name.startswith('$'):
        return AddressingForm.RESTART_VECTOR
    if name.isdigit():
        return AddressingForm.BIT_INDEX
    if name == 'n8':
        return AddressingForm.IMMEDIATE_DATA8
    if name == 'n16':
        return AddressingForm.IMMEDIATE_DATA16
    if name == 'e8':
        return AddressingForm.IMMEDIATE_RELATIVE8
    if name == 'a8':
        return AddressingForm.ABSOLUTE_INDIRECT if not immediate else AddressingForm.IMMEDIATE_ADDRESS8
    if name == 'a16':
        return AddressingForm.ABSOLUTE_INDIRECT if not immediate else AddressingForm.IMMEDIATE_ADDRESS16
    if name == 'C' and not immediate:
        return AddressingForm.HIGH_RAM_C_INDIRECT
    if name == 'SP' and increment:
        return AddressingForm.SP_PLUS_RELATIVE8
    if name in _REGISTER_NAMES:
        if not immediate:
            if increment:
                return AddressingForm.REGISTER_INDIRECT_POST_INCREMENT
            if decrement:
                return AddressingForm.REGISTER_INDIRECT_POST_DECREMENT
            return AddressingForm.REGISTER_INDIRECT
        return AddressingForm.REGISTER
    return AddressingForm.IMPLIED


def _infer_operand_role(operation: str, operands: list[Mapping[str, object]], index: int, operand: Mapping[str, object]) -> str:
    name = str(operand['name'])
    if name in _CONDITION_CODES:
        return 'condition'
    if name.startswith('$'):
        return 'restart_vector'
    if name.isdigit():
        return 'bit_index'
    if not operands:
        return 'implied'
    if len(operands) == 1:
        return 'operand'
    if operation in {'LD', 'LDH', 'POP'} and index == 0:
        return 'destination'
    if operation in {'PUSH'} and index == 0:
        return 'source'
    if index == 0:
        return 'lhs'
    return 'rhs'


def _format_operand(operand: Mapping[str, object]) -> str:
    name = str(operand['name'])
    immediate = bool(operand.get('immediate', True))
    increment = bool(operand.get('increment', False))
    decrement = bool(operand.get('decrement', False))
    if name == 'C' and not immediate:
        return '[C]'
    if not immediate:
        suffix = '+' if increment else '-' if decrement else ''
        return f'[' + name + suffix + ']'
    return name


def _build_mnemonic(operation: str, operands: list[Mapping[str, object]], prefix: PrefixClass) -> str:
    if operation == 'PREFIX' and prefix is PrefixClass.NONE:
        return 'PREFIX CB'
    if operation == 'LD' and len(operands) == 3 and str(operands[1]['name']) == 'SP' and bool(operands[1].get('increment', False)):
        return f"LD {_format_operand(operands[0])}, SP + {_format_operand(operands[2])}"
    rendered = ', '.join(_format_operand(operand) for operand in operands)
    return operation if not rendered else f"{operation} {rendered}"


def _infer_family(operation: str, operands: list[Mapping[str, object]], prefix: PrefixClass, is_illegal: bool) -> str:
    if is_illegal:
        return 'invalid'
    if prefix is PrefixClass.CB or operation in _BITOPS_MNEMONICS:
        return 'bitops'
    if operation in {'JP', 'JR', 'CALL', 'RET', 'RETI', 'RST'}:
        return 'control_flow'
    if operation in {'DI', 'EI', 'HALT', 'STOP'}:
        return 'interrupt_control'
    if operation in {'NOP', 'PREFIX'}:
        return 'control_misc'
    if operation in {'PUSH', 'POP'}:
        return 'stack'
    if operation in {'LD', 'LDH'}:
        return 'memory_load' if any(not bool(operand.get('immediate', True)) for operand in operands) else 'load'
    if operation in {'ADD', 'ADC', 'SUB', 'SBC', 'AND', 'XOR', 'OR', 'CP', 'INC', 'DEC', 'DAA', 'CPL', 'SCF', 'CCF'}:
        uses_16_bit_lane = False
        if operation == 'ADD' and operands and str(operands[0]['name']) in {'HL', 'SP'}:
            uses_16_bit_lane = True
        if operation in {'INC', 'DEC'} and operands and str(operands[0]['name']) in {'BC', 'DE', 'HL', 'SP'} and bool(operands[0].get('immediate', True)):
            uses_16_bit_lane = True
        return 'alu16' if uses_16_bit_lane else 'alu8'
    return 'control_misc'


def _default_compare_scope(family: str) -> CompareScope:
    return {
        'invalid': CompareScope.INVALID_OPCODE,
        'bitops': CompareScope.BITOPS,
        'control_flow': CompareScope.CONTROL_FLOW,
        'interrupt_control': CompareScope.INTERRUPT_CONTROL,
        'control_misc': CompareScope.CONTROL_MISC,
        'stack': CompareScope.STACK,
        'memory_load': CompareScope.MEMORY,
        'load': CompareScope.LOAD,
        'alu8': CompareScope.ALU8,
        'alu16': CompareScope.ALU16,
    }[family]


def _build_operands(operation: str, operands: list[Mapping[str, object]]) -> tuple[OperandMetadata, ...]:
    built = []
    for index, operand in enumerate(operands):
        built.append(
            OperandMetadata(
                name=str(operand['name']),
                role=_infer_operand_role(operation, operands, index, operand),
                addressing_form=_infer_addressing_form(operand),
                immediate=bool(operand.get('immediate', True)),
                width_bytes=int(operand.get('bytes', 0)),
                increment=bool(operand.get('increment', False)),
                decrement=bool(operand.get('decrement', False)),
            )
        )
    return tuple(built)


def _build_opcode(prefix: PrefixClass, opcode: int, raw_entry: Mapping[str, object]) -> OpcodeMetadata:
    operation = str(raw_entry['mnemonic'])
    operands = list(raw_entry['operands'])
    is_illegal = operation.startswith('ILLEGAL_')
    family = _infer_family(operation, operands, prefix, is_illegal)
    return OpcodeMetadata(
        opcode=opcode,
        prefix=prefix,
        operation=operation,
        mnemonic=_build_mnemonic(operation, operands, prefix),
        length_bytes=int(raw_entry['bytes']),
        cycles_tstates=_normalize_cycles(list(raw_entry['cycles'])),
        flag_policy={name: _FLAG_EFFECT_MAP[value] for name, value in raw_entry['flags'].items()},
        operands=_build_operands(operation, operands),
        default_compare_scope=_default_compare_scope(family),
        family=family,
        is_illegal=is_illegal,
    )


