from __future__ import annotations

"""Canonical SM83 opcode metadata for iceboy.

Sources:
- https://gbdev.io/pandocs/CPU_Instruction_Set.html
- https://gbdev.io/gb-opcodes/optables/
- https://gbdev.io/gb-opcodes/Opcodes.json

The raw opcode facts below are derived from the official gbdev opcode JSON export.
This module keeps those facts local to the repo and normalizes them into a richer
Python representation used by generators, coverage tooling, and future HDL lookup
materialization.

Cycle tuples are stored as `(not_taken, taken)` for conditional control-flow
instructions. Single-cycle instructions use a one-element tuple.
"""

from dataclasses import dataclass
from enum import Enum
from typing import Mapping


class PrefixClass(str, Enum):
    NONE = 'none'
    CB = 'CB'


class FlagEffect(str, Enum):
    AFFECTED = 'affected'
    RESET = 'reset'
    SET = 'set'
    UNCHANGED = 'unchanged'


class CompareScope(str, Enum):
    CONTROL_MISC = 'control_misc'
    CONTROL_FLOW = 'control_flow'
    INTERRUPT_CONTROL = 'interrupt_control'
    LOAD = 'load'
    MEMORY = 'memory'
    STACK = 'stack'
    ALU8 = 'alu8'
    ALU16 = 'alu16'
    BITOPS = 'bitops'
    INVALID_OPCODE = 'invalid_opcode'


class AddressingForm(str, Enum):
    REGISTER = 'register'
    REGISTER_INDIRECT = 'register_indirect'
    REGISTER_INDIRECT_POST_INCREMENT = 'register_indirect_post_increment'
    REGISTER_INDIRECT_POST_DECREMENT = 'register_indirect_post_decrement'
    IMMEDIATE_DATA8 = 'immediate_data8'
    IMMEDIATE_DATA16 = 'immediate_data16'
    IMMEDIATE_RELATIVE8 = 'immediate_relative8'
    IMMEDIATE_ADDRESS8 = 'immediate_address8'
    IMMEDIATE_ADDRESS16 = 'immediate_address16'
    ABSOLUTE_INDIRECT = 'absolute_indirect'
    HIGH_RAM_C_INDIRECT = 'high_ram_c_indirect'
    CONDITION_CODE = 'condition_code'
    BIT_INDEX = 'bit_index'
    RESTART_VECTOR = 'restart_vector'
    SP_PLUS_RELATIVE8 = 'sp_plus_relative8'
    IMPLIED = 'implied'


@dataclass(frozen=True)
class OperandMetadata:
    name: str
    role: str
    addressing_form: AddressingForm
    immediate: bool
    width_bytes: int
    increment: bool = False
    decrement: bool = False


@dataclass(frozen=True)
class OpcodeMetadata:
    opcode: int
    prefix: PrefixClass
    operation: str
    mnemonic: str
    length_bytes: int
    cycles_tstates: tuple[int, ...]
    flag_policy: Mapping[str, FlagEffect]
    operands: tuple[OperandMetadata, ...]
    default_compare_scope: CompareScope
    family: str
    is_illegal: bool


